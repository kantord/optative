#[allow(clippy::too_many_arguments)]
fn eval_and_reconcile<R, L>(
    path_str: &str,
    src: &str,
    is_jsx: bool,
    entries: &[crate::EsEntry],
    setup: fn(&Ctx<'_>) -> rquickjs::Result<()>,
    dry_run: bool,
    quiet: bool,
    limit: Option<usize>,
    resolver: R,
    loader: L,
) -> Result<RunStats, crate::ScriptError>
where
    R: Resolver + 'static,
    L: Loader + 'static,
{
    if limit.is_some() && !quiet {
        eprintln!(
            "esto run: --limit is set — which items get selected is not stable across runs \
             (item order comes from an unordered internal HashMap); re-running may pick a \
             different subset."
        );
    }

    let (_runtime, context) = build_runtime(entries, resolver, loader)
        .map_err(|e| crate::ScriptError::Worker(e.to_string()))?;

    let stats = context
        .with(|ctx| -> Result<RunStats, String> {
            let result: rquickjs::Result<RunStats> = (|| {
                setup(&ctx)?;

                let leaves = collect_leaves(&ctx, path_str, src, is_jsx)?;

                let mut by_kind: HashMap<u32, (Object, Vec<Leaf>)> = HashMap::new();
                for leaf in leaves {
                    let entry = by_kind
                        .entry(leaf.kind_id)
                        .or_insert_with(|| (leaf.kind.clone(), vec![]));
                    entry.1.push(leaf);
                }

                let mut stats = RunStats {
                    enter: 0,
                    update: 0,
                    exit: 0,
                    unchanged: 0,
                    errors: 0,
                    limited: 0,
                };

                for (_, (kind_obj, kind_leaves)) in by_kind {
                    let res = reconcile_kind(&ctx, &kind_obj, kind_leaves, dry_run, quiet, limit)?;
                    stats.enter += res.enter;
                    stats.update += res.update;
                    stats.exit += res.exit;
                    stats.unchanged += res.unchanged;
                    stats.errors += res.errors;
                    stats.limited += res.limited;
                }

                Ok(stats)
            })();
            // Unwrap the real JS exception (message + stack) while `ctx` is still
            // alive — once this closure returns, the exception state is gone and
            // all that's left is rquickjs's contentless `Error::Exception` Display.
            result.map_err(|e| describe_js_error(&ctx, e))
        })
        .map_err(crate::ScriptError::Worker)?;

    if !quiet {
        let limited_note = if stats.limited > 0 {
            format!(", {} limited", stats.limited)
        } else {
            String::new()
        };
        eprintln!(
            "reconciled: {} enter, {} update, {} exit ({} unchanged{limited_note})",
            stats.enter, stats.update, stats.exit, stats.unchanged
        );
    }

    Ok(stats)
}
